export class Nutrients
{
    calories:number;
    carbohydrates:number;
    fat:number;
    protein:number;
    constructor(calor:number,carbo:number,fat:number,protein:number)
    {
        this.calories=calor;
        this.carbohydrates=carbo;
        this.fat=fat;
        this.protein=protein;
    }
}